package hr.fer.zemris.optjava.dz5.part2;

public class PermutationPair {

	private Permutation permutation1;
	private Permutation permutation2;
	
	public PermutationPair(Permutation permutation1, Permutation permutation2) {
		super();
		this.permutation1 = permutation1;
		this.permutation2 = permutation2;
	}
	
	public Permutation getPermutation1() {
		return permutation1;
	}
	
	public Permutation getPermutation2() {
		return permutation2;
	}
	
}
